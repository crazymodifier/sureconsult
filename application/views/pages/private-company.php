          <li>Limited liability: In a private limited company, there is a limited liability, which means the members of the company are not at the risk of losing their private assets. If a company fails, the shareholders are liable to sell their assets for payment</li>
          <li>Less number of shareholders: Unlike a public company that requires seven shareholders, a private limited company can be started with just two shareholders.</li>
          <li>Ownership: As the company’s shares are owned by investors, founders and management, the owners are at the liberty of transferring and selling their shares to others.</li>
          <li>Uninterrupted existence: As mentioned earlier, the company stays a legal entity until it is legally shut down, the company runs even after the death or departure of any member.</li>
          <li>Funding: A company can raise equity capital from persons or entities interested in becoming a shareholder. Entrepreneurs can raise money from angel investors, venture capital firms, private equity firms and hedge funds.</li>
          <li>Separate Legal Entity: A private limited company is recognised as a separate entity legally with perpetual existence. It can have a PAN number, bank accounts, licenses, approvals, contracts, assets and liabilities in its unique name.</li>
